export type Language = "ar" | "en";

export function t(key: keyof typeof translations["ar"] | string, currentLang: Language): string {
  const dict = translations[currentLang] || translations["ar"];
  const val = (dict as any)[key];
  if (val !== undefined) return val;
  
  // Fallback to Arabic dict, then key itself
  const arDict = translations["ar"];
  return (arDict as any)[key] || String(key);
}

export function formatString(text: string, params: Record<string, string | number>): string {
  let result = text;
  for (const [key, val] of Object.entries(params)) {
    result = result.replace(`{${key}}`, String(val));
  }
  return result;
}
